// Logical and Realtional Operators
public class java_17 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        if(a&&b)
        {
            System.out.println("Y");
        }
        else{
            System.out.println("N");
        }

        if(a || b)
        {
            System.out.println("Y");
        }
        else{
            System.out.println("N");
        }

        System.out.print("!a is ");
        System.out.println(!a);
    }
}
