public class java_12 {
    public static void main(String[] args) {

        // Crypting and Decrypting the grade
        char grade = 'A';
        grade = (char) (grade + 8);
        System.out.println(grade);

        // Decrypting the grade
        grade = (char) (grade - 8);
        System.out.println(grade);
    }
}
